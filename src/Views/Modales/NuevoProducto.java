    public static boolean isNumero(String Numero) {
        try {
            if (Numero.contains(".")) {
                Double.parseDouble(Numero);
            } else {
                Integer.parseInt(Numero);
            }

            return true;
        } catch (Exception ex) {
            return false;
        }
    }

