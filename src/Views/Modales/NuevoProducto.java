    private void BtnNewProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnNewProductoActionPerformed
        producto p = new producto();
        p.setSerieproducto(this.codigo.getText().trim());
        p.setNombreProducto(this.nombre.getText().trim());
        p.setCosto(new BigDecimal(this.costoF.getText().trim()));
        p.setCantidad(Integer.parseInt(this.cantidad.getText().trim()));
        p.setPrecio_venta(new BigDecimal(this.precioVenta.getText().trim()));
        p.setStock(new BigDecimal(this.stock.getText().trim()));
        p.setCategoria((categoria) categoria.getSelectedItem());
        p.setIvaP((iva) iva.getSelectedItem());
        p.setUnidad((Unidad) uni.getSelectedItem());

        prc.getPr().getListProductos().add(p);
        this.dispose();
    }//GEN-LAST:event_BtnNewProductoActionPerformed

